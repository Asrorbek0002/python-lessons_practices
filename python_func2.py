def get_Area(r,pi=3.14159):
    """Doiraning yuzini qaytaruvchi dastur"""
    return pi*r**2

def get_Perimeter(r,pi=3.14159):
    """Doirani perimetrini qaytatuvchi funksiya"""
    return 2*pi*r

